    private void btnIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIngresarActionPerformed

        String user = jTextUser.getText();
        String pw = new String(jPassword.getPassword());

        try {
            if (udao.getUserByName(user, pw).getNombre().equals(user) && udao.getUserByName(user, pw).getContraseña().equals(pw)) {
                Principal i = new Principal(udao.getUserByName(user, pw).getCargo());
                i.setVisible(true);
                this.setVisible(false);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Usuario o clave incorrectos!");
        }
    }//GEN-LAST:event_btnIngresarActionPerformed

