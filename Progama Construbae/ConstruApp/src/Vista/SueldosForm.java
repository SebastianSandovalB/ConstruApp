    private void payActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_payActionPerformed
        modelo = (DefaultTableModel) jTable1.getModel();
        String nom = (String) modelo.getValueAt(jTable1.getSelectedRow(), 0);
        String cargo = (String) modelo.getValueAt(jTable1.getSelectedRow(), 1);
        int sueldo = (int) modelo.getValueAt(jTable1.getSelectedRow(), 2);

        int respuesta = JOptionPane.showConfirmDialog(this, "Desea pagar sueldo de : " + nom + " ---- " + cargo + "?", "Confirmar", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
        if (respuesta == JOptionPane.YES_OPTION) {
            gdao.nuevoGasto("Sueldo", nom + " " + cargo, sueldo, 1, sueldo, fecha());
            JOptionPane.showMessageDialog(null, "Pago ingresado con exito");
        } else {
            System.out.println("no acepto");
        }


    }//GEN-LAST:event_payActionPerformed

    private void payAllActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_payAllActionPerformed
        modelo = (DefaultTableModel) jTable1.getModel();
        int respuesta = JOptionPane.showConfirmDialog(this, "Desea pagar todos los sueldos?", "Confirmar", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);

        if (respuesta == JOptionPane.YES_OPTION) {
            for (int i = 0; i < modelo.getRowCount(); i++) {
                String nom = (String) modelo.getValueAt(i, 0);
                String cargo = (String) modelo.getValueAt(i, 1);
                int sueldo = (int) modelo.getValueAt(i, 2);
                gdao.nuevoGasto("Sueldo", nom + " " + cargo, sueldo, 1, sueldo, fecha());
            }
            JOptionPane.showMessageDialog(null, "Pago ingresado con exito");
        } else {
            System.out.println("no acepto");
        }
    }//GEN-LAST:event_payAllActionPerformed

