    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        modelo = (DefaultTableModel) jTable1.getModel();
        String nom = (String) modelo.getValueAt(jTable1.getSelectedRow(), 0);
        String carac = (String) modelo.getValueAt(jTable1.getSelectedRow(), 1);

        int respuesta = JOptionPane.showConfirmDialog(this, "Desea borrar a: " + nom + " de la lista de empleados?", "Confirmar", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
        if (respuesta == JOptionPane.YES_OPTION) {
            pdao.delete(nom, carac);
            LimpiarTabla();
            btnActualizarActionPerformed(evt);
            JOptionPane.showMessageDialog(null, "Empleado eliminado con exito.");
        } else {
            System.out.println("no acepto");
        }

    }//GEN-LAST:event_jButton2ActionPerformed

    private String fecha() {

        Date calendario = new Date();

        SimpleDateFormat sdf = new SimpleDateFormat("yyyy/MM/dd");

        String FechaCorrecta = sdf.format(calendario);

        return FechaCorrecta;
    }

