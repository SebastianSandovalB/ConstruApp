    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed

        modelo = (DefaultTableModel) jTable1.getModel();

        String nom = (String) modelo.getValueAt(jTable1.getSelectedRow(), 0);
        String desc = (String) modelo.getValueAt(jTable1.getSelectedRow(), 1);
        int pre = (int) modelo.getValueAt(jTable1.getSelectedRow(), 2);
        int can = (int) modelo.getValueAt(jTable1.getSelectedRow(), 3);
        int tot = (int) modelo.getValueAt(jTable1.getSelectedRow(), 4);
        String fech = (String) modelo.getValueAt(jTable1.getSelectedRow(), 5);

        int respuesta = JOptionPane.showConfirmDialog(this, "Desea borrar el gasto: " + nom + "?", "Confirmar", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
        if (respuesta == JOptionPane.YES_OPTION) {
            gdao.delete(nom, desc, pre, can, tot, fech);
            removeRows();
            jButton1ActionPerformed(evt);
        } else {
            System.out.println("no acepto");
        }


    }//GEN-LAST:event_btnEliminarActionPerformed

