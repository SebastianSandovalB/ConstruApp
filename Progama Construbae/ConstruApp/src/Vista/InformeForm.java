    void buscarMaterial(String valor) {
        DecimalFormat formatea = new DecimalFormat("###,###.##");
        int cont = 0;
        try {
            removeRows();
            modelo = (DefaultTableModel) jTable1.getModel();

            Object[] object = new Object[6];

            String sql = "select * from Gasto WHERE CONCAT (nombre,'',descripcion) LIKE '%" + valor + "%'";
            ps = con.prepareStatement(sql);
            rs = ps.executeQuery();
            while (rs.next()) {
                object[0] = rs.getString(2);
                object[1] = rs.getString(3);
                object[2] = rs.getInt(4);
                object[3] = rs.getInt(5);
                object[4] = rs.getInt(6);
                object[5] = rs.getString(7);

                modelo.addRow(object);
                int tot = rs.getInt(6);
                
                cont = cont + tot;
            }

            jTable1.setModel(modelo);

            int neto = (int) (cont / 1.19);
            int iva = cont - neto;
            jTxtIva.setText("$ " + formatea.format(iva));
            jTxtNeto.setText("$ " + formatea.format(neto));
            jTxtTotal.setText("$ " + formatea.format(cont));

        } catch (SQLException e) {
            System.out.println(e.getMessage());
        }

    }

    void calcularTotales() {
        modelo = (DefaultTableModel) jTable1.getModel();
        DecimalFormat formatea = new DecimalFormat("###,###.##");
        int cont = 0;
        for (int i = 0; i < modelo.getRowCount(); i++) {
            int value = (int) modelo.getValueAt(i, 4);
            cont = cont + value;
        }
        int neto = (int) (cont / 1.19);
        int iva = cont - neto;
        jTxtIva.setText("$ " + formatea.format(iva));
        jTxtNeto.setText("$ " + formatea.format(neto));
        jTxtTotal.setText("$ " + formatea.format(cont));
    }

